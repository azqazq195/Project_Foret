package freeboard.bean;

public class FreeBoard {
	
	private int freeboard_no; 				//게시판 글 번호
	private int freeboard_hit; 				//조회수 (안쓸듯?)
	private int freeboard_like_count;		//좋아요 개수
	private int freeboard_comment_count;	//댓글 개수
	private String member_id;				//회원번호(글쓴이)
	private String freeboard_subject;		//제목
	private String freeboard_content;		//내용
	private String freeboard_write_date;	//등록시간
	private String freeboard_edit_date;		//수정시간
	
	public FreeBoard() {
	}

	public int getFreeboard_no() {
		return freeboard_no;
	}

	public void setFreeboard_no(int freeboard_no) {
		this.freeboard_no = freeboard_no;
	}

	public int getFreeboard_hit() {
		return freeboard_hit;
	}

	public void setFreeboard_hit(int freeboard_hit) {
		this.freeboard_hit = freeboard_hit;
	}

	public int getFreeboard_like_count() {
		return freeboard_like_count;
	}

	public void setFreeboard_like_count(int freeboard_like_count) {
		this.freeboard_like_count = freeboard_like_count;
	}

	public int getFreeboard_comment_count() {
		return freeboard_comment_count;
	}

	public void setFreeboard_comment_count(int freeboard_comment_count) {
		this.freeboard_comment_count = freeboard_comment_count;
	}

	public String getMember_id() {
		return member_id;
	}

	public void setMember_id(String member_id) {
		this.member_id = member_id;
	}

	public String getFreeboard_subject() {
		return freeboard_subject;
	}

	public void setFreeboard_subject(String freeboard_subject) {
		this.freeboard_subject = freeboard_subject;
	}

	public String getFreeboard_content() {
		return freeboard_content;
	}

	public void setFreeboard_content(String freeboard_content) {
		this.freeboard_content = freeboard_content;
	}

	public String getFreeboard_write_date() {
		return freeboard_write_date;
	}

	public void setFreeboard_write_date(String freeboard_write_date) {
		this.freeboard_write_date = freeboard_write_date;
	}

	public String getFreeboard_edit_date() {
		return freeboard_edit_date;
	}

	public void setFreeboard_edit_date(String freeboard_edit_date) {
		this.freeboard_edit_date = freeboard_edit_date;
	}

	

}
